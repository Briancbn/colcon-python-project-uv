import asyncio
from pathlib import Path
import sysconfig
import os
from subprocess import Popen

from colcon_core.package_discovery import discover_packages
from colcon_core.package_identification \
    import get_package_identification_extensions
from colcon_core.python_install_path import get_python_install_path
from colcon_python_project_uv.task.python.project.uv import UV_EXECUTABLE
from colcon_python_project_uv.venv.location import get_uv_venv_path
from colcon_python_project_uv.venv.location import get_venv_path
from colcon_python_project_uv.venv.location import create_uv_venv_path
from colcon_python_project_uv.shell.sh import create_activate_script \
    as create_activate_script_sh
import tomli_w

UV_VENV_PYTHON_VERSION = '.python-version'
UV_VENV_SPEC = 'pyproject.toml'
UV_VENV_SPEC_HEADER = b'# GENERATED BY COLCON VENV\n'
PYTHON_VERSION = sysconfig.get_python_version()

def get_uv_venv_metadata(args):
    extensions = get_package_identification_extensions()
    descriptors = discover_packages(args, extensions)

    venv_pkg_name = Path.cwd().name
    doc = {
        "project": {
            "name": venv_pkg_name,
            "version": "0.1.0",
            "description": "colcon uv venv management package",
            "requires-python": f'>={PYTHON_VERSION}',
            "dependencies": []
        },
        "tool": {
            "uv": {
                "sources": {},
                "override-dependencies": [],
            }
        }
    }
    doc_deps = doc["project"]["dependencies"]
    override_deps = doc["tool"]["uv"]["override-dependencies"]
    uv_sources = doc["tool"]["uv"]["sources"]
    for desc in descriptors:
        spec = desc.metadata.get('python_project_spec')
        if spec is None:
            continue

        build_backend = spec.get('build-system', {}).get('build-backend')

        # ignore setuptools legacy build
        if build_backend == 'setuptools.build_meta:__legacy__':
            continue

        # TODO(Briancbn): better validation
        doc_deps.append(desc.name)
        uv_sources.update({ desc.name: {"path": str(desc.path.absolute()), "editable": True} })
        override_deps.append(f"{desc.name} @ file://{str(desc.path.absolute())}")

    return doc

def dump_uv_venv_metadata(metadata, *, install_base):
    uv_venv_path = Path(install_base) / get_uv_venv_path()
    create_uv_venv_path(install_base)
    spec_path = uv_venv_path / UV_VENV_SPEC
    with open(spec_path, "wb") as f:
        f.write(UV_VENV_SPEC_HEADER)
        tomli_w.dump(metadata, f)

    python_version_path = uv_venv_path / UV_VENV_PYTHON_VERSION
    with open(python_version_path, "w") as f:
        f.writelines([
            UV_VENV_SPEC_HEADER.decode('utf-8'),
            PYTHON_VERSION])

def create_activate_script(*, install_base):
    prefix_path = Path(install_base)
    venv_path = Path(install_base) / get_venv_path()
    python_lib_path = get_python_install_path('purelib', {'base': venv_path})
    if not python_lib_path.exists():
        return None

    return create_activate_script_sh(
        prefix_path.absolute(), venv_path.absolute(), python_lib_path.absolute())


def prepare_uv_venv(args):
    metadata = get_uv_venv_metadata(args)
    dump_uv_venv_metadata(metadata, install_base=args.install_base)


def _uv_sync(install_base):
    if UV_EXECUTABLE is None:
        raise RuntimeError("Could not find 'uv' executable")

    cmd = [
        UV_EXECUTABLE,
        'sync',
        '--no-install-local',
    ]
    cwd = Path(install_base) / get_uv_venv_path()

    process = Popen(cmd, cwd=cwd,
                               shell=(os.name == 'nt'))
    while process.returncode is None:
        try:
            process.communicate()
        except KeyboardInterrupt:
            pass
    return process.returncode


def sync_uv_venv(args):
    prepare_uv_venv(args)

    rc = _uv_sync(args.install_base)
    if rc:
        return rc

    create_activate_script(install_base=args.install_base)
